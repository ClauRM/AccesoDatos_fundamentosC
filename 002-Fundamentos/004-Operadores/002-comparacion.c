#include <stdio.h>

int main(int argc, char *argv[])
{
    printf("Se comparan dos numeros 4 y 3. En los resultados: 1 es true y 0 es false \n");
    printf("MAYOR QUE (4 > 3): %i \n", (4 > 3));
    printf("MENOR QUE (4 < 3): %i \n", (4 < 3));
    printf("MAYOR O IGUAL QUE (4 >= 3): %i \n", (4 >= 3));
    printf("MENOR O IGUAL QUE (4 <= 3): %i \n", (4 <= 3));
    printf("IGUAL QUE (4 == 3): %i \n", (4 == 3));
    printf("NO ES IGUAL QUE (4 != 3): %i \n", (4 != 3));
    return 0;
}