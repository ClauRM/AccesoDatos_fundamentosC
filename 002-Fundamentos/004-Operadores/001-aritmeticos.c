#include <stdio.h>

int main(int argc, char *argv[])
{
    printf("SUMA: %i \n", (4 + 3));                        // SUMA
    printf("RESTA: %i \n", (4 - 3));                       // RESTA
    printf("MULTIPLICACION: %i \n", (4 * 3));              // MULTIPLICACION
    printf("DIVISION: %i \n", (4 / 3));                    // DIVISION CON PARTE ENTERA
    printf("RESTO ENTERO DE LA DIVISION: %i \n", (4 % 3)); // RESTO
    return 0;
}